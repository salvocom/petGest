<?

class Button
{
	protected $name;
	protected $id;
	protected $type;
	protected $page;
	protected $getvalues;
	
	function __construct($_name, $_id, $_type, $_page, $_getvalues) 
	{	
		$this->name = $_name;
		$this->id = $_id;
		$this->type = $_type;
		$this->page = $_page;
		$this->getvalues = $_getvalues;
	}

	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getName()
	{
		return $this->name;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getID()
	{
		return $this->id;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getType()
	{
		return $this->type;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getAction()
	{
		return $this->action;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getPage()
	{
		return $this->page;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function getGetValues()
	{
		return $this->getvalues;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setName($value)
	{
		$this->name = $value;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setID($value)
	{
		$this->id = $value;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setType($value)
	{
		$this->type = $value;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setAction($value)
	{
		$this->action = $value;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setPage($value)
	{
		$this->page = $value;
	}
	
	//////////////////////////////////////////////////////////////////	
	///
	///
	///
	//////////////////////////////////////////////////////////////////	
	function setGetValues($values)
	{
			$this->getvalues = $values;
	}

}

?>
